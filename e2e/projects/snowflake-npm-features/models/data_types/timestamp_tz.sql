SELECT 
  CAST('2023-01-01 12:00:00' AS TIMESTAMP_TZ) AS from_string,
  CAST(1234567890 AS TIMESTAMP_TZ) AS from_unix_epoch,
  CAST(TO_DATE('2023-01-01') AS TIMESTAMP_TZ) AS from_date,
  CAST('1234567890' AS TIMESTAMP_TZ) AS from_time,
  CAST('2023-01-01 12:00:00 -7:00' AS TIMESTAMP_TZ) AS from_timestamp_tz;
